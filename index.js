// SOMA
function soma(number1, number2){
    let soma = number1 + number2
    return 'A soma dos valores é ' + soma
}

let resultadoDaSoma = soma(10, 5)

console.log(resultadoDaSoma)

//SUBTRAÇÃO
function subtracao(number1, number2){
    let subtracao = number1 - number2
    return 'A soma dos valores é ' + subtracao
}

let resultadoDaSubtracao = subtracao(15, 10)

console.log(resultadoDaSubtracao)

